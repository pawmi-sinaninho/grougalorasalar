def svg_document(cells: list[dict[str, Any]], boundary: dict[str, Any]) -> str:
    parts = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{REFERENCE_WIDTH}" height="{REFERENCE_HEIGHT}" viewBox="0 0 {REFERENCE_WIDTH} {REFERENCE_HEIGHT}">',
        '<rect width="100%" height="100%" fill="#000"/>',
        '<g id="playable-cells" fill="#fff" stroke="#777" stroke-width="0.8">',
    ]
    for cell in cells:
        points = " ".join(f'{p["x"]:.6f},{p["y"]:.6f}' for p in cell["referencePolygon"])
        parts.append(
            f'<polygon id="{cell["stableId"]}" data-cell-id="{cell["id"]}" data-x="{cell["x"]}" data-y="{cell["y"]}" data-parity="{cell["parity"]}" points="{points}"/>'
        )
    parts.append("</g>")
    exterior = " ".join(f'{p["x"]:.6f},{p["y"]:.6f}' for p in boundary["exteriorReferencePixels"])
    parts.append(f'<polyline id="arena-boundary" points="{exterior}" fill="none" stroke="#00ff00" stroke-width="3"/>')
    parts.append("</svg>\n")
    return "\n".join(parts)


def render_assets(project_root: Path, cells_doc: dict[str, Any], boundary_doc: dict[str, Any]) -> None:
    assets = project_root / "assets" / "arena"
    assets.mkdir(parents=True, exist_ok=True)
    cells = cells_doc["cells"]
    mask = np.zeros((REFERENCE_HEIGHT, REFERENCE_WIDTH), dtype=np.uint8)
    centres = np.zeros((REFERENCE_HEIGHT, REFERENCE_WIDTH, 4), dtype=np.uint8)
    polygons = np.zeros((REFERENCE_HEIGHT, REFERENCE_WIDTH, 4), dtype=np.uint8)
    boundary_image = np.zeros((REFERENCE_HEIGHT, REFERENCE_WIDTH, 4), dtype=np.uint8)
    for cell in cells:
        polygon = np.array([[round(p["x"]), round(p["y"])] for p in cell["referencePolygon"]], dtype=np.int32)
        cv2.fillPoly(mask, [polygon], 255)
        colour = (255, 255, 255, 255) if cell["parity"] == "light" else (150, 150, 150, 255)
        cv2.fillPoly(polygons, [polygon], colour)
        cv2.polylines(polygons, [polygon], True, (40, 40, 40, 255), 1, cv2.LINE_AA)
        cx = round(cell["referencePixelCenter"]["x"])
        cy = round(cell["referencePixelCenter"]["y"])
        cv2.circle(centres, (cx, cy), 3, (255, 255, 255, 255), -1, cv2.LINE_AA)
        if cell["boundary"]:
            cv2.fillPoly(boundary_image, [polygon], (0, 255, 255, 90))
            cv2.polylines(boundary_image, [polygon], True, (0, 255, 255, 255), 2, cv2.LINE_AA)
    cv2.imwrite(str(assets / "grougalorasalar-mask.png"), mask)
    cv2.imwrite(str(assets / "grougalorasalar-cell-centers.png"), centres)
    cv2.imwrite(str(assets / "grougalorasalar-cell-polygons.png"), polygons)
    cv2.imwrite(str(assets / "grougalorasalar-boundary-debug.png"), boundary_image)
    (assets / "grougalorasalar-mask.svg").write_text(
        svg_document(cells, boundary_doc), encoding="utf-8"
    )

    reference = cv2.imread(str(project_root / "assets" / "reference" / "user_reference.png"), cv2.IMREAD_COLOR)
    if reference is None:
        raise RuntimeError("Missing canonical reference image")
    overlay = reference.copy()
    for cell in cells:
        polygon = np.array([[round(p["x"]), round(p["y"])] for p in cell["referencePolygon"]], dtype=np.int32)
        colour = (0, 255, 0) if not cell["boundary"] else (0, 165, 255)
        cv2.polylines(overlay, [polygon], True, colour, 1, cv2.LINE_AA)
        cx = round(cell["referencePixelCenter"]["x"])
        cy = round(cell["referencePixelCenter"]["y"])
        cv2.circle(overlay, (cx, cy), 2, colour, -1, cv2.LINE_AA)
        cv2.putText(overlay, str(cell["id"]), (cx - 8, cy + 4), cv2.FONT_HERSHEY_SIMPLEX, 0.22, colour, 1, cv2.LINE_AA)
    cv2.imwrite(str(assets / "grougalorasalar-debug-overlay.png"), overlay)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--project-root", type=Path, default=Path(__file__).resolve().parents[1])
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()
    project_root = args.project_root.resolve()
    cells_doc, boundary_doc, edge_report = build_cells(project_root)
    expected = {
        project_root / "data" / "arena" / "grougalorasalar.cells.json": cells_doc,
        project_root / "data" / "arena" / "grougalorasalar.boundary.json": boundary_doc,
        project_root / "data" / "arena" / "grougalorasalar.registration.json": registration_document(project_root),
        project_root / "data" / "arena" / "grougalorasalar.landmarks.json": landmarks_document(),
        project_root / "VALIDATION" / "edge-review-report.json": edge_report,
    }
    if args.check:
        for path, value in expected.items():
            actual = load_json(path)
            if actual != value:
                raise SystemExit(f"Generated artefact is stale: {path}")
