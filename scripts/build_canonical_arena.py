def load_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def dump_json(path: Path, value: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(value, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for block in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(block)
    return digest.hexdigest()


def project(x: float, y: float) -> tuple[float, float]:
    point = ORIGIN + x * BASIS_X + y * BASIS_Y
    return round(float(point[0]), 6), round(float(point[1]), 6)


def polygon_for_cell(x: int, y: int) -> list[dict[str, float]]:
    # Logical square corners become the four visible isometric diamond corners.
    corners = [
        (x - 0.5, y - 0.5),
        (x + 0.5, y - 0.5),
        (x + 0.5, y + 0.5),
        (x - 0.5, y + 0.5),
    ]
    return [{"x": px, "y": py} for px, py in (project(cx, cy) for cx, cy in corners)]


def source_authority(old_class: str) -> tuple[str, float, str, list[str]]:
    if old_class in {"walkableConfirmed", "walkableObserved"}:
        return "visible_empty_map", 1.0, "none", [
            "empty-arena-raster",
            "combat-screenshot-cross-check",
        ]
    if old_class == "boundaryUnverified":
        return "user_boundary_annotation", 1.0, "partial_edge", [
            "user-green-boundary-centre-rule",
            "empty-arena-raster",
            "grid-continuation",
            "parity-cross-check",
        ]
    if old_class == "occludedUnknown":
        return "user_hidden_cell_annotation", 1.0, "architecture_or_foreground", [
            "user-hidden-cell-overlay",
            "grid-continuation",
            "parity-cross-check",
            "combat-screenshot-cross-check",
        ]
    raise ValueError(f"Unsupported old class: {old_class}")


def build_cells(project_root: Path) -> tuple[dict[str, Any], dict[str, Any], dict[str, Any]]:
    old = load_json(project_root / "data" / "arena" / "arena-model.draft-v0.5.0.json")
    old_class_by_cell: dict[tuple[int, int], str] = {}
    for class_name, items in old["cellSets"].items():
        if class_name == "permanentBlocked":
            continue
        for item in items:
            key = (int(item["x"]), int(item["y"]))
            if key in old_class_by_cell:
                raise ValueError(f"Duplicate draft cell {key}")
            old_class_by_cell[key] = class_name

    # The exact canonical footprint is the user-confirmed fixed arena envelope.
    # It can also be generated by the four integer inequalities below.
    generated = {
        (x, y)
        for x in range(-12, 14)
        for y in range(-12, 14)
        if -11 <= x + y <= 13 and -13 <= x - y <= 13
    }
    if generated != set(old_class_by_cell):
        missing = sorted(generated - set(old_class_by_cell))
        extra = sorted(set(old_class_by_cell) - generated)
        raise ValueError(f"Draft/envelope mismatch: missing={missing}, extra={extra}")

    ordered = sorted(generated, key=lambda item: (item[1], item[0]))
    id_by_cell = {cell: index for index, cell in enumerate(ordered)}
    cells: list[dict[str, Any]] = []
    for index, (x, y) in enumerate(ordered):
        old_class = old_class_by_cell[(x, y)]
        authority, confidence, occlusion, provenance = source_authority(old_class)
        neighbours = []
        for direction, (dx, dy) in DIRECTIONS.items():
            neighbour = (x + dx, y + dy)
            if neighbour in generated:
                neighbours.append(
                    {
                        "direction": direction,
                        "id": id_by_cell[neighbour],
                        "x": neighbour[0],
                        "y": neighbour[1],
                    }
                )
        boundary = len(neighbours) < 4
        cx, cy = project(x, y)
        cells.append(
            {
                "id": index,
                "stableId": f"C{index:03d}",
                "x": x,
                "y": y,
                "parity": "light" if (x + y) % 2 == 0 else "dark",
                "sourceAuthority": authority,
                "confidence": confidence,
                "boundary": boundary,
                "occlusion": occlusion,
                "referencePixelCenter": {"x": cx, "y": cy},
                "referencePolygon": polygon_for_cell(x, y),
                "neighbors": neighbours,
                "provenance": provenance,
                "supersededDraftClass": old_class,
            }
        )

    by_sum = Counter(x + y for x, y in generated)
    by_diff = Counter(x - y for x, y in generated)
    direct_total = len(generated)
    sum_total = sum(by_sum.values())
    diff_total = sum(by_diff.values())
    if not direct_total == sum_total == diff_total == 338:
        raise ValueError((direct_total, sum_total, diff_total))

    # Parity invariant: all logical orthogonal neighbours must flip colour.
    parity_errors = []
    for x, y in generated:
        for dx, dy in DIRECTIONS.values():
            neighbour = (x + dx, y + dy)
            if neighbour in generated and ((x + y) & 1) == ((neighbour[0] + neighbour[1]) & 1):
                parity_errors.append(((x, y), neighbour))
    if parity_errors:
        raise ValueError(f"Parity errors: {parity_errors[:5]}")

    cells_doc = {
        "schemaVersion": SCHEMA_VERSION,
        "arenaModelId": ARENA_ID,
        "status": "canonical_user_verified_fixed_footprint",
        "sourceOfTruth": True,
        "totalCells": direct_total,
        "coordinateSystem": {
            "type": "integer_square_grid_rendered_isometrically",
            "originCell": {"x": 0, "y": 0},
            "xAxisScreenDirection": "down_right",
            "yAxisScreenDirection": "down_left",
            "referenceSize": {"width": REFERENCE_WIDTH, "height": REFERENCE_HEIGHT},
            "originPixel": {"x": float(ORIGIN[0]), "y": float(ORIGIN[1])},
            "basisX": {"x": float(BASIS_X[0]), "y": float(BASIS_X[1])},
            "basisY": {"x": float(BASIS_Y[0]), "y": float(BASIS_Y[1])},
            "projectionFormula": "pixel = origin + x*basisX + y*basisY",
            "cellPolygonFormula": "logical square [x±0.5,y±0.5] projected through the same affine basis",
        },
        "footprint": {
            "integerInequalities": [
                "-12 <= x <= 13",
                "-12 <= y <= 13",
                "-11 <= x + y <= 13",
                "-13 <= x - y <= 13",
            ],
            "holes": [],
            "permanentBlockedCells": [],
        },
        "parityRule": {
            "formula": "(x + y) mod 2",
            "zeroParity": "light",
            "oneParity": "dark",
            "neighborInvariant": "orthogonal logical neighbours always have different parity",
            "visualEvidence": {
                "source": "empty-arena-reference",
                "medianLabLZeroParity": 208.0,
                "medianLabLOneParity": 182.5,
                "medianSeparation": 25.5,
                "note": "Visible brightness is a cross-check only; logical parity is coordinate-derived.",
            },
        },
        "countVerification": {
            "directUniqueCoordinateCount": direct_total,
            "byXPlusY": {str(key): by_sum[key] for key in sorted(by_sum)},
            "byXPlusYTotal": sum_total,
            "byXMinusY": {str(key): by_diff[key] for key in sorted(by_diff)},
            "byXMinusYTotal": diff_total,
            "allMethodsAgree": True,
        },
        "sourceReferences": [
            {
                "id": "retained-combat-reference",
                "path": "assets/reference/user_reference.png",
                "role": "canonical_reference_coordinate_system",
            },
            {
                "id": "empty-arena-raster",
                "path": "assets/reference/empty_arena.jpeg",
                "role": "grid_edges_centres_parity_and_static_landmarks",
            },
            {
                "id": "user-hidden-cell-overlay",
                "path": "assets/reference/user_hidden_cells_annotation.png",
                "role": "occluded_outer_cell_confirmation",
            },
            {
                "id": "user-green-boundary-centre-rule",
                "path": None,
                "role": "outermost_marked_cell_centres_are_included",
                "retention": "conversation_evidence_applied; original byte asset unavailable in active phase runtime",
            },
            {
                "id": "phase7-real-combat-fixtures",
                "path": "packages/fixtures/real/phase7/",
                "role": "cross_screenshot_registration_and_occupancy_evidence",
            },
        ],
        "cells": cells,
    }

    # Build the exterior cycle without geometry dependencies. Shared cell edges
    # cancel; the remaining degree-2 graph is the hole-free arena boundary.
    edge_counts: dict[tuple[tuple[int, int], tuple[int, int]], int] = {}
    for x, y in generated:
        vertices = [
            (2 * x - 1, 2 * y - 1),
            (2 * x + 1, 2 * y - 1),
            (2 * x + 1, 2 * y + 1),
            (2 * x - 1, 2 * y + 1),
        ]
        for start, end in zip(vertices, vertices[1:] + vertices[:1]):
            key = tuple(sorted((start, end)))
            edge_counts[key] = edge_counts.get(key, 0) + 1
    exterior_edges = [edge for edge, count in edge_counts.items() if count == 1]
    adjacency: dict[tuple[int, int], list[tuple[int, int]]] = {}
    for start, end in exterior_edges:
        adjacency.setdefault(start, []).append(end)
        adjacency.setdefault(end, []).append(start)
    if any(len(neighbours) != 2 for neighbours in adjacency.values()):
        raise ValueError("Canonical boundary is not one closed degree-2 cycle")
    start = min(adjacency)
    cycle = [start]
    previous = None
    current = start
    while True:
        candidates = sorted(adjacency[current])
        next_vertex = candidates[0] if candidates[0] != previous else candidates[1]
        if next_vertex == start:
            cycle.append(start)
            break
        cycle.append(next_vertex)
        previous, current = current, next_vertex
        if len(cycle) > len(exterior_edges) + 1:
            raise ValueError("Boundary cycle tracing did not terminate")
    if len(cycle) != len(exterior_edges) + 1:
        raise ValueError("Boundary contains multiple cycles or a hole")
    exterior = [(x2 / 2.0, y2 / 2.0) for x2, y2 in cycle]
    boundary_cells = [cell for cell in cells if cell["boundary"]]
    boundary_edges = []
    for cell in boundary_cells:
        x, y = cell["x"], cell["y"]
        present = {(n["x"], n["y"]) for n in cell["neighbors"]}
        for direction, (dx, dy) in DIRECTIONS.items():
            if (x + dx, y + dy) not in present:
                boundary_edges.append(
                    {
                        "cellId": cell["id"],
                        "stableId": cell["stableId"],
                        "cell": {"x": x, "y": y},
                        "direction": direction,
                    }
                )
    boundary_doc = {
        "schemaVersion": SCHEMA_VERSION,
        "arenaModelId": ARENA_ID,
        "totalBoundaryCells": len(boundary_cells),
        "totalBoundaryEdges": len(boundary_edges),
        "boundaryCells": [
            {"id": cell["id"], "stableId": cell["stableId"], "x": cell["x"], "y": cell["y"]}
            for cell in boundary_cells
        ],
        "boundaryEdges": boundary_edges,
        "exteriorLogicalVertices": [
            {"x": float(x), "y": float(y)} for x, y in exterior
        ],
        "exteriorReferencePixels": [
            {"x": px, "y": py} for px, py in (project(x, y) for x, y in exterior)
        ],
        "holes": [],
    }
    edge_report = refine_boundary_documents(project_root, cells_doc, boundary_doc)
    return cells_doc, boundary_doc, edge_report


def write_csv(path: Path, cells: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.writer(handle)
        writer.writerow(
            [
                "id",
                "stable_id",
                "x",
                "y",
                "parity",
                "source_authority",
                "confidence",
                "boundary",
                "occlusion",
                "reference_center_x",
                "reference_center_y",
                "neighbor_ids",
            ]
        )
        for cell in cells:
            writer.writerow(
                [
                    cell["id"],
                    cell["stableId"],
                    cell["x"],
                    cell["y"],
                    cell["parity"],
                    cell["sourceAuthority"],
                    f'{cell["confidence"]:.6f}',
                    str(cell["boundary"]).lower(),
                    cell["occlusion"],
                    f'{cell["referencePixelCenter"]["x"]:.6f}',
                    f'{cell["referencePixelCenter"]["y"]:.6f}',
                    "|".join(str(item["id"]) for item in cell["neighbors"]),
                ]
            )


